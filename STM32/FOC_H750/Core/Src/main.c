/* USER CODE BEGIN 4 */
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc) {
    if(hadc->Instance == ADC1) {
        if(is_motor_running == 0) {
            // Disable Run, Locking signal svPWM for safety
            TIM1->CCR1 = 0; TIM1->CCR2 = 0; TIM1->CCR3 = 0;
            return;
        }

        // Read i current
        uint32_t adc_a = HAL_ADC_GetValue(&hadc1);
		uint32_t adc_b = HAL_ADC_GetValue(&hadc2);
		clarke_park.Ia = ((float)adc_a - ADC_OFFSET) * ADC_GAIN;
		clarke_park.Ib = ((float)adc_b - ADC_OFFSET) * ADC_GAIN;
        // Read Encoder
		uint32_t encoder_count = __HAL_TIM_GET_COUNTER(&htim2);
		float theta_mech = ((float)(encoder_count % 10000) / 10000.0f) * (2.0f * 3.14159f);
		float theta_elec = theta_mech * 4.0f;
		while (theta_elec >= (2.0f * 3.14159f)) theta_elec -= (2.0f * 3.14159f);
		clarke_park.Angle = theta_elec;

		ClarkePark_Update(&clarke_park);

        // Speed Target in debug
        // limit speed lower than 100 rpm
        if (target_speed_rpm > 100.0f) target_speed_rpm = 100.0f;
        if (target_speed_rpm < -100.0f) target_speed_rpm = -100.0f; // Quay nghịch

        // FOC Controller
        float Iq_ref = PI_Update(&pi_speed, target_speed_rpm, measured_speed_rpm);

		// PI_i Current
		float Vd = PI_Update(&pi_id, 0.0f, clarke_park.Id); // Id = 0
		float Vq = PI_Update(&pi_iq, Iq_ref, clarke_park.Iq);

		// Inv_park & Svpwm
		float cos_t = cos(clarke_park.Angle);
		float sin_t = sin(clarke_park.Angle);
		svpwm.Valpha = (Vd * cos_t) - (Vq * sin_t);
		svpwm.Vbeta  = (Vd * sin_t) + (Vq * cos_t);

		SVPWM_Update(&svpwm);

		// Export PWM to TIM1
		float duty_multiplier = ARR_VALUE / svpwm.Tsw;
		TIM1->CCR1 = (uint32_t)(svpwm.Ts1 * duty_multiplier);
		TIM1->CCR2 = (uint32_t)(svpwm.Ts2 * duty_multiplier);
		TIM1->CCR3 = (uint32_t)(svpwm.Ts3 * duty_multiplier);
    }
}
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
    if(GPIO_Pin == GPIO_PIN_13) {
        is_motor_running = !is_motor_running; // invert state Start/Stop

        if(is_motor_running) {
            // When starting, reset block integral for PI avoid shaft shocks
            pi_id.I_prev = 0;
            pi_iq.I_prev = 0;
        }
    }
}
